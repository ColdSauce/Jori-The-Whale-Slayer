	public void checkWhereToSpawn() {
		switch (jori.getLocationMemory()) {
		case 2:
			collisionX = (132 * 32);
			collisionY = (32 * 32);
			break;

		case 3:
			collisionX = (63 * 32);
			collisionY = (134 * 32);
			break;

		case 4:
			collisionX = (51 * 32);
			collisionY = (87 * 32);
			break;

		case 5:
			collisionX = (109 * 32);
			collisionY = (117 * 32);
			break;
		}
		// Since the map is going opposite of what the user knows,
		//
		fmx = -1 * collisionX;
		fmy = -1 * collisionY;
	}

	@Override
	public void update(GameContainer gc, StateBasedGame s, int delta)
			throws SlickException {
		// This just tests whether or not the ship is at the coordinates of
		// the hometown land, the whale wars land.. etc. If it is, you go
		// to it
		checkHome();
		checkCaves();
		checkWhale();
		checkWest();
		checkEast();
		if (homeArray[collisionX / 32][collisionY / 32] == 1) {
			jori.changeRan(false);
			s.enterState(2);
			jori.changeLocationMemory(12);
		}
		if (eastArray[collisionX / 32][collisionY / 32] == 1) {
			jori.changeRan(false);
			s.enterState(5);
			jori.changeLocationMemory(12);
		}
		if (whaleArray[collisionX / 32][collisionY / 32] == 1) {
			jori.changeRan(false);
			s.enterState(3);
			jori.changeLocationMemory(12);
		}
		if (westArray[collisionX / 32][collisionY / 32] == 1) {
			jori.changeRan(false);
			s.enterState(4);
			jori.changeLocationMemory(12);
		}
		if (cavesArray[collisionX / 32][collisionY / 32] == 1) {
			jori.changeRan(false);
			jori.changeLocationMemory(12);
			if (jori.getKarma() < 0)
				s.enterState(15);
			else if (jori.getKarma() > 0)
				s.enterState(16);

		}

		locationX = (collisionX / 8) + 32;
		locationY = (collisionY / 8) + 32;
		int edgeOfTheRightMap = 193;
		int edgeOfTheTopMap = 0;
		int edgeOfTheBottomMap = 167;
		int edgeOfTheLeftMap = 0;

		// this makes the pit blue again
		if (jori.getTimesTalked() == 3) {
			if (collisionX == (100 * 32) && collisionY == 16 * 32)
				if (gc.getInput().isKeyPressed(Input.KEY_ENTER))
					if (jori.canChangePit())
						fullMap = new BigImage("/res/Maps/fullMap.png");
		}

		gc.setTargetFrameRate(120);
		if (!jori.didRun()) {
			// runs only once
			checkWhereToSpawn();

		}
		jori.changeRan(true);
		// first it tests if you're not in the edges of the map
		// so that it doesn't give you an index out of bounds
		if (collisionX != 0 && collisionY != 0 && collisionX != 83) {
			canMoveRight = !(mapArray[(collisionX / 32) + 1][(collisionY / 32)] == 1);
			canMoveLeft = !(mapArray[(collisionX / 32) - 1][(collisionY / 32)] == 1);
			canMoveDown = !(mapArray[(collisionX / 32)][(collisionY / 32) + 1] == 1);
			canMoveUp = !(mapArray[(collisionX / 32)][(collisionY / 32) - 1] == 1);
		}
		if (input.isKeyDown(Input.KEY_LEFT) && canMoveLeft
				&& collisionX != edgeOfTheLeftMap) {
			jj = new Image("res/pics of chars/shipLeft.png");
			fmx += 32;
			collisionX -= 32;
		} else if (input.isKeyDown(Input.KEY_RIGHT) && canMoveRight
				&& collisionX / 32 != edgeOfTheRightMap) {
			jj = new Image("res/pics of chars/shipRight.png");

			fmx -= 32;
			collisionX += 32;
		} else if (input.isKeyDown(Input.KEY_UP) && canMoveUp
				&& collisionY != edgeOfTheTopMap) {
			jj = new Image("res/pics of chars/shipUp.png");

			fmy += 32;
			collisionY -= 32;
		} else if (input.isKeyDown(Input.KEY_DOWN) && canMoveDown
				&& collisionY / 32 != edgeOfTheBottomMap) {
			jj = new Image("res/pics of chars/shipDown.png");

			fmy -= 32;
			collisionY += 32;
		}
		this.makeCollisionEasier();
		if (randomNumber[collisionX / 32][collisionY / 32] < 10000) {
			if (randomNumber[collisionX / 32][collisionY / 32] > 5000) {
				gc.setTargetFrameRate(60);
				jori.changeRan(false);
				s.enterState(7);
			}
		}
		pressedM = (input.isKeyDown(Input.KEY_M));
		randomNumber[collisionX / 32][collisionY / 32] = 0;
	}

